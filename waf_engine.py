# ── WAF detector ─────────────────────────────────────────────────────────────

async def fingerprint_waf(base_url: str, log: Callable) -> dict:
    """
    Send malicious probes to a non-existent endpoint and analyse the block screen.
    Returns WAF info dict: {detected: bool, vendor: str, strategy: str, block_status: int}
    """
    ok, reason = scope_policy.validate_target(base_url, action="scan")
    if not ok:
        await log("[WAF] Skipped by ScopePolicy: {}".format(reason))
        return {"detected": False, "vendor": "ScopePolicy", "confidence": 0,
                "strategy": "passive_only", "blocked": True}
    result = {
        "detected":     False,
        "vendor":       "Unknown",
        "strategy":     "normal",   # passive_only | conservative | normal | intensive_authorized
        "block_status": 0,
        "block_body":   "",
        "confidence":   0,
    }

    await log("[WAF] Fingerprinting WAF on {}".format(base_url))

    # First probe: completely non-existent endpoint (establishes 404 baseline)
    base = base_url.rstrip("/")
    try:
        async with httpx.AsyncClient(
            headers=HEADERS, verify=False,
            follow_redirects=False, timeout=10
        ) as client:
            # Baseline — clean request to non-existent path
            r_clean = await client.get(
                base + "/burpollama-nonexistent-8x7k2",
                headers=HEADERS
            )
            clean_status = r_clean.status_code
            clean_body   = r_clean.text[:500]

            # Now send malicious probe
            for probe in WAF_PROBE_PAYLOADS:
                r_waf = await client.get(base + probe, headers=HEADERS)

                # If response differs from clean → WAF likely intercepted
                if r_waf.status_code != clean_status or r_waf.text[:200] != clean_body[:200]:
                    vendor, confidence = _identify_vendor(r_waf)
                    if confidence > 0:
                        result.update({
                            "detected":     True,
                            "vendor":       vendor,
                            "block_status": r_waf.status_code,
                            "block_body":   r_waf.text[:300],
                            "confidence":   confidence,
                            "strategy":     _pick_strategy(vendor),
                        })
                        await log("[WAF] ⚠ Detected: {} (confidence {}%) — using '{}' strategy".format(
                            vendor, confidence, result["strategy"]
                        ))
                        return result

    except Exception as e:
        await log("[WAF] Probe error: {}".format(e))

    await log("[WAF] No WAF detected — using normal strategy")
    return result


def _identify_vendor(response: httpx.Response) -> tuple[str, int]:
    """Match a response against WAF signatures. Returns (vendor, confidence 0-100)."""
    resp_headers = {k.lower(): v.lower() for k, v in response.headers.items()}
    body         = response.text.lower()
    server       = resp_headers.get("server", "")
    best_vendor  = "Unknown"
    best_score   = 0

    for vendor, sigs in WAF_SIGNATURES.items():
        score = 0

        # Header matches (weight 3 each)
        for h in sigs["headers"]:
            if h.lower() in resp_headers:
                score += 3

        # Body matches (weight 2 each)
        for b in sigs["body"]:
            if b.lower() in body:
                score += 2

        # Server header (weight 4)
        for s in sigs["server"]:
            if s.lower() in server:
                score += 4

        # Status code match (weight 1)
        if response.status_code in sigs["status"]:
            score += 1

        if score > best_score:
            best_score  = score
            best_vendor = vendor

    # Convert score to confidence %
    confidence = min(95, best_score * 12) if best_score > 0 else 0
    return best_vendor, confidence


def _pick_strategy(vendor: str) -> str:
    """Pick the appropriate authorized scan strategy based on WAF vendor."""
    slow_waf     = {"ModSecurity", "F5 BIG-IP ASM", "Barracuda", "Fortinet FortiWeb"}
    conservative_waf  = {"Cloudflare", "Akamai", "Imperva / Incapsula"}
    standard_waf = {"Nginx Rate Limit", "Wordfence"}

    if vendor in slow_waf:
        return "conservative"
    elif vendor in conservative_waf:
        return "conservative"
    elif vendor in standard_waf:
        return "normal"
    return "normal"


# ── Adaptive throttle manager ─────────────────────────────────────────────────

class ThrottleManager:
    """
    Global traffic monitor for all hunt workers.
    When any worker hits a WAF block or rate limit, all workers pause
    and the request rate is backed off exponentially.

    Usage:
        throttle = ThrottleManager()
        async with throttle.gate():
            response = await client.get(url)
            throttle.record(response.status_code, response.text)
    """

    WAF_BODY_INDICATORS = [
        "cloudflare", "incapsula", "sucuri", "barracuda",
        "access denied", "blocked", "bot", "captcha",
        "security check", "ddos", "unusual activity",
    ]

    def __init__(self):
        self._lock               = asyncio.Lock()
        self._paused             = False
        self._backoff            = 0.5
        self._backoff_mult       = 1.0
        self._max_mult           = 16.0
        self._last_block         = 0.0
        self._recovery_secs      = 45
        self._total_blocks       = 0
        self._broadcast_fn       = None
        # v3.3: HOST_DEAD_WAF threshold
        self._consecutive_blocks = 0
        self._max_consecutive    = 10    # mark host dead after this many sequential blocks
        self._host_dead          = False
        self._host_counters      = defaultdict(lambda: {
            "requests": 0,
            "blocks": 0,
            "window": deque(maxlen=500),
            "last_block_reason": "",
            "cooldown_until": 0.0,
        })
        self._global_window      = deque(maxlen=2000)
        self._last_block_reason  = ""
        self._cooldown_until     = 0.0
        self._cooldown_reason    = ""
        self._smart_mode         = True
        self._response_baselines: dict[str, ResponseBaseline] = {}
        self._last_classification: dict[str, dict] = {}

    def set_broadcast(self, fn):
        self._broadcast_fn = fn

    @property
    def host_dead(self) -> bool:
        return self._host_dead

    @property
    def current_delay(self) -> float:
        return self._backoff * self._backoff_mult

    def _host(self, url: str = "") -> str:
        try:
            return urlparse(url).netloc or "unknown"
        except Exception:
            return "unknown"

    def _retry_after_seconds(self, headers: dict | None = None) -> float:
        if not headers:
            return 0.0
        value = ""
        for k, v in headers.items():
            if str(k).lower() == "retry-after":
                value = str(v).strip()
                break
        if not value:
            return 0.0
        try:
            return max(0.0, min(300.0, float(value)))
        except ValueError:
            try:
                return max(0.0, min(300.0, parsedate_to_datetime(value).timestamp() - time.time()))
            except Exception:
                return 0.0

    def _reason_for_block(self, status: int, body: str = "", headers: dict | None = None) -> str:
        retry = self._retry_after_seconds(headers)
        body_lower = (body or "").lower()
        if retry:
            return "Server requested Retry-After {:.0f}s".format(retry)
        if status == 429:
            return "Rate limited with HTTP 429"
        if status == 403:
            return "Forbidden or WAF block with HTTP 403"
        if status == 503:
            return "Service unavailable or WAF challenge with HTTP 503"
        if status == 200 and any(c in body_lower for c in CHALLENGE_INDICATORS):
            return "WAF/challenge page detected"
        return "Target is blocking requests"

    def recommendation(self) -> str:
        if self._host_dead or self._consecutive_blocks >= 6 or self._backoff_mult >= 8:
            return "Target is blocking requests. Switch from Deep Authorized Scan to Bounty Scan or Safe Passive Scan."
        if self._consecutive_blocks >= 3 or self._backoff_mult >= 4:
            return "Target is showing blocking behavior. Prefer Bounty Scan or Safe Passive Scan."
        return "Throttle is healthy."

    def status(self) -> dict:
        now = time.monotonic()
        recent = [x for x in self._global_window if now - x[0] <= 60]
        recent_blocks = [x for x in recent if x[2]]
        host_stats = {}
        known_hosts = set(self._host_counters) | set(self._last_classification)
        for host in known_hosts:
            data = self._host_counters[host]
            window = list(data["window"])
            recent_host = [x for x in window if now - x[0] <= 60]
            recent_host_blocks = [x for x in recent_host if x[1]]
            host_stats[host] = {
                "requests": data["requests"],
                "blocks": data["blocks"],
                "requests_per_minute": len(recent_host),
                "block_rate": round((len(recent_host_blocks) / max(1, len(recent_host))) * 100, 1),
                "last_block_reason": data["last_block_reason"],
                "cooldown_remaining_secs": max(0, round(data["cooldown_until"] - now, 1)),
                "last_response_classification": self._last_classification.get(host, {}),
            }
        return {
            "smart_mode": self._smart_mode,
            "requests_per_minute": len(recent),
            "block_rate": round((len(recent_blocks) / max(1, len(recent))) * 100, 1),
            "last_block_reason": self._last_block_reason,
            "cooldown_active": now < self._cooldown_until,
            "cooldown_remaining_secs": max(0, round(self._cooldown_until - now, 1)),
            "cooldown_reason": self._cooldown_reason,
            "current_throttle_multiplier": self._backoff_mult,
            "current_delay": round(self.current_delay, 2),
            "total_blocks": self._total_blocks,
            "consecutive_blocks": self._consecutive_blocks,
            "host_dead": self._host_dead,
            "recommendation": self.recommendation(),
            "hosts": host_stats,
        }

    async def gate(self):
        """Async context manager — waits if throttled before proceeding."""
        return _ThrottleGate(self)

    async def _wait(self):
        """Wait the current throttle delay."""
        now = time.monotonic()
        if self._cooldown_until > now:
            await asyncio.sleep(max(0.0, self._cooldown_until - now))
        delay = self.current_delay
        if delay > 0.1:
            await asyncio.sleep(delay)

    async def record_request(self, url: str = ""):
        host = self._host(url)
        now = time.monotonic()
        async with self._lock:
            data = self._host_counters[host]
            data["requests"] += 1
            data["window"].append((now, False))
            self._global_window.append((now, host, False))

    async def record_block(self, status: int, body: str = "", url: str = "", headers: dict | None = None):
        """Call when a worker receives a block/rate-limit response."""
        import random as _rand
        async with self._lock:
            host = self._host(url)
            host_data = self._host_counters[host]
            self._total_blocks       += 1
            self._consecutive_blocks += 1
            self._last_block          = time.monotonic()
            self._backoff_mult        = min(self._max_mult, self._backoff_mult * 2)
            self._paused              = True
            classification = self._last_classification.get(host, {})
            if classification.get("is_block"):
                reason = "{} (score {}): {}".format(
                    str(classification.get("classification", "WAF block")).replace("_", " "),
                    classification.get("score", 0),
                    ", ".join(classification.get("reasons", [])[:3])
                    or self._reason_for_block(status, body, headers),
                )
            else:
                reason = self._reason_for_block(status, body, headers)
            self._last_block_reason = reason
            host_data["blocks"] += 1
            host_data["last_block_reason"] = reason
            marked = False
            for i in range(len(host_data["window"]) - 1, -1, -1):
                ts, blocked = host_data["window"][i]
                if not blocked and self._last_block - ts <= 10:
                    host_data["window"][i] = (ts, True)
                    marked = True
                    break
            global_marked = False
            for i in range(len(self._global_window) - 1, -1, -1):
                ts, h, blocked = self._global_window[i]
                if h == host and not blocked and self._last_block - ts <= 10:
                    self._global_window[i] = (ts, h, True)
                    global_marked = True
                    break
            if not marked:
                host_data["window"].append((self._last_block, True))
            if not global_marked:
                self._global_window.append((self._last_block, host, True))

            # v3.3: Jitter prevents thundering-herd re-requests after backoff
            retry_after = self._retry_after_seconds(headers)
            base_wait  = max(retry_after, min(30, self._backoff_mult * 2))
            if self._consecutive_blocks >= 3:
                base_wait = max(base_wait, min(120, 15 * (self._consecutive_blocks - 2)))
            jitter     = _rand.uniform(0, 1.0)
            pause_time = base_wait + jitter
            self._cooldown_until = max(self._cooldown_until, self._last_block + pause_time)
            self._cooldown_reason = reason
            host_data["cooldown_until"] = max(host_data["cooldown_until"], self._last_block + pause_time)

            print("[Throttle] BLOCK #{} (status={}) — pause {:.1f}s (mult={:.1f}x jitter={:.2f})".format(
                self._consecutive_blocks, status, pause_time,
                self._backoff_mult, jitter))

            # v3.3: HOST_DEAD_WAF — abort scan after _max_consecutive blocks
            if self._consecutive_blocks >= self._max_consecutive and not self._host_dead:
                self._host_dead = True
                dead_msg = (
                    "[Throttle] HOST_DEAD_WAF: {} consecutive blocks at max backoff. "
                    "Switching to passive-only mode.".format(self._consecutive_blocks)
                )
                print(dead_msg)
                if self._broadcast_fn:
                    await self._broadcast_fn({
                        "type":    "host_dead_waf",
                        "message": dead_msg,
                        "blocks":  self._consecutive_blocks,
                        "url":     url,
                    })

            if self._broadcast_fn:
                await self._broadcast_fn({
                    "type":    "throttle_warning",
                    "message": "Target is blocking requests",
                    "status":  status,
                    "delay":   pause_time,
                    "mult":    self._backoff_mult,
                    "jitter":  jitter,
                    "url":     url,
                    "host":    host,
                    "reason":  reason,
                    "recommendation": self.recommendation(),
                    "consecutive": self._consecutive_blocks,
                    "host_dead":   self._host_dead,
                    "status_snapshot": self.status(),
                })

            await asyncio.sleep(pause_time)
            self._paused = False

    async def maybe_recover(self):
        """Reduce multiplier if no blocks for recovery_secs."""
        async with self._lock:
            if (time.monotonic() - self._last_block > self._recovery_secs
                    and self._backoff_mult > 1.0):
                self._backoff_mult       = max(1.0, self._backoff_mult / 2)
                self._consecutive_blocks = 0   # reset on recovery
                self._cooldown_reason    = ""

    def calibrate_responses(
        self,
        url: str,
        *,
        normal_status: int,
        normal_body: str,
        normal_headers: dict | None = None,
        block_status: int | None = None,
        block_body: str = "",
        block_headers: dict | None = None,
    ) -> None:
        host = self._host(url)
        self._response_baselines[host] = ResponseBaseline(
            normal=ResponseFingerprint.build(
                normal_status, normal_body[:16000], normal_headers
            ),
            blocked=(
                ResponseFingerprint.build(
                    int(block_status), block_body[:16000], block_headers
                )
                if block_status is not None
                else None
            ),
        )

    def classify_response(
        self,
        status: int,
        body: str = "",
        headers: dict | None = None,
        url: str = "",
    ) -> dict:
        host = self._host(url)
        result = classify_response(
            ResponseFingerprint.build(status, body, headers),
            self._response_baselines.get(host),
        )
        self._last_classification[host] = result
        return result

    def is_block_response(
        self,
        status: int,
        body: str = "",
        headers: dict | None = None,
        url: str = "",
    ) -> bool:
        """Classify WAF blocks without treating every application 403 as one."""
        return bool(
            self.classify_response(status, body, headers, url).get("is_block")
        )

    async def record_network_error(self, url: str = ""):
        async with self._lock:
            import random as _rand
            wait = min(8.0, self._backoff * self._backoff_mult) + _rand.uniform(0, 0.5)
            self._last_block_reason = "Network error; retrying conservatively"
        await asyncio.sleep(wait)


class _ThrottleGate:
    """Async context manager returned by ThrottleManager.gate()"""
    def __init__(self, mgr: ThrottleManager):
        self._mgr = mgr

    async def __aenter__(self):
        # Wait if paused
        while self._mgr._paused:
            await asyncio.sleep(0.5)
        # Apply current delay
        await self._mgr._wait()
        await self._mgr.maybe_recover()
        return self

    async def __aexit__(self, *args):
        pass


