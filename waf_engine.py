# ── Challenge page detection (v3.3) ───────────────────────────────────────────
# Modern WAFs return HTTP 200 with a JS challenge or CAPTCHA instead of 403.
# If the hunt engine receives these it may mistake the challenge HTML for a
# real reflection or structural change, producing false positives.

CHALLENGE_INDICATORS = [
    # Cloudflare challenge / CAPTCHA variants
    "cf_chl_opt", "challenge-form", "jschl_vc", "jschl_answer",
    "__cf_chl_captcha_tk__", "__cf_chl_f_tk__",
    "cf-please-wait", "cf_chl_prog",
    # Generic reCAPTCHA / hCaptcha / Turnstile
    "recaptcha/api.js", "hcaptcha.com", "challenges.cloudflare.com",
    # Akamai Bot Manager
    "_abck=", "ak_bmsc", "bm_sz",
    # Imperva / Incapsula
    "incap_ses", "visid_incap",
    # PerimeterX / Human Security
    "_px3", "px-captcha",
    # Generic JS challenge patterns
    "document.cookie", "location.reload", "setTimeout(", "setInterval(",
    # DataDome
    "datadome", "dd_referrer",
]

_CHALLENGE_SCRIPT_RE = re.compile(
    r'<script[^>]*>[^<]{0,200}(document\.cookie\s*=|location\.href\s*=|'
    r'setTimeout\s*\(|challenge)',
    re.IGNORECASE | re.DOTALL
)


def is_challenge_page(response: httpx.Response) -> bool:
    """
    Detect a CAPTCHA or JavaScript challenge page disguised as HTTP 200.
    Called by hunt workers before processing any 200 response body.
    """
    if response.status_code != 200:
        return False
    body = response.text
    body_lower = body.lower()
    # Check keyword indicators
    if any(ind.lower() in body_lower for ind in CHALLENGE_INDICATORS):
        return True
    # Check for inline JS challenge scripts
    if _CHALLENGE_SCRIPT_RE.search(body):
        return True
    return False


# ── Benign probes for early WAF detection (v3.3) ─────────────────────────────
# Sent BEFORE blunt attack payloads to detect WAF presence without raising
# the target's threat score or triggering IP-level captcha challenges.

BENIGN_ANOMALY_PROBES = [
    # Non-standard HTTP method — many WAFs respond with 405 + vendor header
    {
        "method":  "PROPFIND",
        "path":    "/",
        "headers": {},
    },
    # Oversized Accept header — triggers header inspection without looking malicious
    {
        "method":  "GET",
        "path":    "/",
        "headers": {"Accept": "text/html, " + ("a/b, " * 150)},
    },
    # Unknown HTTP method — probes method validation without attack signatures
    {
        "method":  "BLKBURP",
        "path":    "/",
        "headers": {},
    },
    # Invalid Transfer-Encoding — triggers protocol normalization layer
    {
        "method":  "GET",
        "path":    "/",
        "headers": {"Transfer-Encoding": "xchunked"},
    },
]


async def fingerprint_waf_v2(base_url: str, log: Callable) -> dict:
    """
    v3.3 Two-stage WAF fingerprinting.

    Stage 1 — Benign anomaly probes (low noise):
      Uses non-standard methods and oversized headers to detect WAF presence
      without triggering IP threat escalation or CAPTCHA issuance.
      If Stage 1 detects a WAF with high confidence, skip Stage 2.

    Stage 2 — Malicious signature probes (only if Stage 1 inconclusive):
      Fires traditional attack payloads ('../', SQLi, XSS) to confirm vendor
      and extract block page signature.

    Returns full WAF info dict including challenge_detected flag.
    """
    ok, reason = scope_policy.validate_target(base_url, action="scan")
    if not ok:
        await log("[WAF] Skipped by ScopePolicy: {}".format(reason))
        return {"detected": False, "vendor": "ScopePolicy", "confidence": 0,
                "strategy": "passive_only", "blocked": True}
    result = {
        "detected":           False,
        "vendor":             "Unknown",
        "strategy":           "normal",
        "block_status":       0,
        "block_body":         "",
        "confidence":         0,
        "challenge_detected": False,   # v3.3: CAPTCHA / JS challenge flag
        "stage":              0,       # which stage confirmed the WAF
    }

    await log("[WAF] v3.3 fingerprinting — {}: benign probes first".format(base_url))
    base = base_url.rstrip("/")

    try:
        async with httpx.AsyncClient(
            headers=HEADERS, verify=False,
            follow_redirects=False, timeout=10
        ) as client:

            # ── Establish clean baseline ──────────────────────────────────────
            r_baseline = await client.get(
                base + "/burpollama-baseline-x9k2", headers=HEADERS
            )
            clean_status = r_baseline.status_code
            clean_body   = r_baseline.text[:300]
            throttle.calibrate_responses(
                base,
                normal_status=r_baseline.status_code,
                normal_body=r_baseline.text,
                normal_headers=dict(r_baseline.headers),
            )

            def calibrate_block(response: httpx.Response) -> None:
                throttle.calibrate_responses(
                    base,
                    normal_status=r_baseline.status_code,
                    normal_body=r_baseline.text,
                    normal_headers=dict(r_baseline.headers),
                    block_status=response.status_code,
                    block_body=response.text,
                    block_headers=dict(response.headers),
                )

            # ── Stage 1: Benign anomaly probes ────────────────────────────────
            for probe in BENIGN_ANOMALY_PROBES:
                try:
                    merged_headers = {**HEADERS, **probe["headers"]}
                    r = await client.request(
                        method  = probe["method"],
                        url     = base + probe["path"],
                        headers = merged_headers,
                        timeout = 6,
                    )
                    # Check for challenge page on any 200 response
                    if is_challenge_page(r):
                        result["challenge_detected"] = True
                        result["detected"]           = True
                        result["stage"]              = 1
                        result["block_status"]       = r.status_code
                        vendor, conf = _identify_vendor(r)
                        result.update({
                            "vendor":     vendor if conf > 0 else "WAF (challenge detected)",
                            "confidence": max(conf, 70),
                            "strategy":   _pick_strategy(vendor),
                            "block_body": r.text[:200],
                        })
                        await log("[WAF] Stage 1: Challenge page detected ({}) — {}".format(
                            probe["method"], result["vendor"]))
                        calibrate_block(r)
                        return result

                    # Response differs from clean baseline → WAF intercepted
                    if r.status_code != clean_status:
                        vendor, conf = _identify_vendor(r)
                        if conf >= 15:   # require some confidence before stopping
                            result.update({
                                "detected":     True,
                                "stage":        1,
                                "vendor":       vendor,
                                "confidence":   conf,
                                "block_status": r.status_code,
                                "strategy":     _pick_strategy(vendor),
                                "block_body":   r.text[:200],
                            })
                            await log("[WAF] Stage 1: {} detected ({}% conf, method={})".format(
                                vendor, conf, probe["method"]))
                            calibrate_block(r)
                            return result

                except httpx.RequestError:
                    pass

            # ── Stage 2: Malicious signature probes (only if Stage 1 missed) ─
            await log("[WAF] Stage 1 inconclusive — running Stage 2 signature probes")
            for probe_path in WAF_PROBE_PAYLOADS:
                try:
                    r = await client.get(base + probe_path, headers=HEADERS)

                    if is_challenge_page(r):
                        result.update({
                            "challenge_detected": True,
                            "detected":           True,
                            "stage":              2,
                            "block_status":       r.status_code,
                        })
                        vendor, conf = _identify_vendor(r)
                        result.update({
                            "vendor":     vendor if conf > 0 else "WAF (challenge)",
                            "confidence": max(conf, 65),
                            "strategy":   _pick_strategy(vendor),
                        })
                        await log("[WAF] Stage 2: Challenge page on probe {}".format(probe_path[:30]))
                        calibrate_block(r)
                        return result

                    if r.status_code != clean_status or r.text[:200] != clean_body[:200]:
                        vendor, conf = _identify_vendor(r)
                        if conf > 0:
                            result.update({
                                "detected":     True,
                                "stage":        2,
                                "vendor":       vendor,
                                "confidence":   conf,
                                "block_status": r.status_code,
                                "strategy":     _pick_strategy(vendor),
                                "block_body":   r.text[:300],
                            })
                            await log("[WAF] Stage 2: {} ({}%)".format(vendor, conf))
                            calibrate_block(r)
                            return result

                except httpx.RequestError:
                    pass

    except Exception as e:
        await log("[WAF] Fingerprint error: {}".format(e))

    if not result["detected"]:
        await log("[WAF] No WAF detected — normal strategy")
    return result
